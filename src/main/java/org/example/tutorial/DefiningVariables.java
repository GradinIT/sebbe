package org.example.tutorial;

public class DefiningVariables {
    public static void main(String[] args) {
        int myInt = 1; // defining a variable of typ int with name myInt and assigning it the value of 1.
        double myDouble = 1.00; // defining a variable of typ double with name myDouble and assigning it the value of 1.00
        float myFloat = (float)1.235;
        byte myByte = 1;
        char myChar = 'H';
        boolean myBoolean = true;
        String myString = "my string value";
        System.out.println(myInt);
        System.out.println(myByte);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myBoolean);
        System.out.println(myChar);
        System.out.println(myString);
    }
}
